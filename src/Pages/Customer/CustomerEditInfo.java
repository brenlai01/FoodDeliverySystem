    private void UpdateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateButtonActionPerformed
        // Get the currently logged-in user
        User currentUser  = CurrentUser .getLoggedInUser ();

        if (currentUser  != null) {
            // Retrieve new username and password from text fields
            String newUsername = NewUsernameTestField.getText().trim();
            String newPassword = NewPasswordTextField.getText().trim();

            // Validate that the new username and password are not empty
            if (newUsername.isEmpty() || newPassword.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Username and Password cannot be empty.", "Input Error", JOptionPane.ERROR_MESSAGE);
                return; // Exit the method if validation fails
            }

            // Update the current user's information
            currentUser .setUsername(newUsername);
            currentUser .setPassword(newPassword);

            // Update the user information in the file
            ArrayList<User> users = FileManager.loadUsers("users.txt"); // Load existing users
            for (int i = 0; i < users.size(); i++) {
                if (users.get(i).getUid().equals(currentUser .getUid())) {
                    users.set(i, currentUser ); // Update the user in the list
                    break;
                }
            }
            FileManager.writeUsers("users.txt", users); // Write updated users back to the file

            // Provide feedback to the user
            JOptionPane.showMessageDialog(this, "Information updated successfully!");

            // Refresh the labels and text fields
            refreshData();
        } else {
            JOptionPane.showMessageDialog(this, "No user is currently logged in.");
        }
    }//GEN-LAST:event_UpdateButtonActionPerformed

